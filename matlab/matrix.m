%% 矩阵的定义与构造
%%
% 直接构造单行的矩阵，如果要多行，用;分隔
A = [1 2 3 5 8 5 4 6]
% 1到9， 步长 为2的等差数列
B = 1:2:9
% 横向三次，纵向两次重复
C = repmat(B, 3, 2)
% 4行4列都是1
D = ones(4, 4)


%% 矩阵的四则运算
%%
A = [1 2 3 4; 5 6 7 8]
B = [1 1 2 2; 2 2 1 1]
C = A + B % A B两个矩阵对应项相加
D = A - B % A B两个矩阵对应项相减
E = A * B' % A B 两个矩阵相乘， A * B的 转至
F = A .* B % A B 两个矩阵对应项相乘
% pinv 返回矩阵 A 的摩尔-彭罗斯伪逆。
G = A / B % 相当于 G = A * pinv(B)  G = A * B的逆矩阵 
H = A ./ B % 对应项相除
